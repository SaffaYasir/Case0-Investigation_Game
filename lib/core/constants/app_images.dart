static String getCharacterImage(String characterName) {
final name = characterName.toLowerCase().trim();

// Case 1 characters
if (name.contains('victoria') || name.contains('montgomery')) return victoria;
if (name.contains('henry') || name.contains('butler')) return henryButler;
if (name.contains('chloe')) return chloe;
if (name.contains('james')) return james;

// Case 2 characters
if (name.contains('alex') || name.contains('carter')) return alexCarter;
if (name.contains('miller') || name.contains('officer')) return officerMiller;
if (name.contains('shadow') || name.contains('informant')) return shadow;
if (name.contains('mayor') || name.contains('assistant') || name.contains('david') || name.contains('chen')) return mayorAssistant;
if (name.contains('cop') || name.contains('police')) return officerMiller;
if (name.contains('suspect') || name.contains('chief')) return officerMiller;

return detectiveSilhouette;
}

static String getCaseBackground(int caseNumber, String sceneId) {
if (caseNumber == 1) return getCase1Background(sceneId);
if (caseNumber == 2) return getCase2Background(sceneId);
return case1MansionScene;
}

static String getCase1Background(String sceneId) {
final id = sceneId.toLowerCase().trim();
if (id.contains('ballroom') || id.contains('ball') || id.contains('scene2') || id.contains('scene_final') || id.contains('accusation')) return case1Ballroom;
if (id.contains('garden') || id.contains('yard') || id.contains('scene12') || id.contains('scene5') || id.contains('scene6')) return case1Garden;
if (id.contains('study') || id.contains('library') || id.contains('office') || id.contains('scene4') || id.contains('scene7') || id.contains('safe')) return case1Study;
if (id.contains('hallway') || id.contains('corridor') || id.contains('entry') || id.contains('scene8')) return case1Hallway;
if (id.contains('scene1') || id.contains('intro')) return case1MansionScene;
return case1MansionScene;
}

static String getCase2Background(String sceneId) {
