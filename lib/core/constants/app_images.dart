// FIXED IMAGE WIDGET
static Widget image(String path, {double? width, double? height, BoxFit fit = BoxFit.cover, Color? color}) {
return Image.asset(
path,
width: width,
height: height,
fit: fit,
color: color,
errorBuilder: (context, error, stackTrace) {
debugPrint('‚ùå Image load failed: $path - $error');
return Container(
width: width,
height: height,
color: Colors.grey[900],
child: Column(
mainAxisAlignment: MainAxisAlignment.center,
children: [
Icon(Icons.image_not_supported, color: Colors.redAccent,
size: width != null ? width * 0.3 : 40),
SizedBox(height: 8),
Text(
'${path.split('/').last}',
textAlign: TextAlign.center,
style: TextStyle(color: Colors.white70, fontSize: 12),
),
],
),
);
},
);
}
